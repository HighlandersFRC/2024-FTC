package org.firstinspires.ftc.teamcode.PathingTool;

public class PathData {
    private double time;
    private double x;
    private double y;
    private double angle;
    private double x_velocity;
    private double y_velocity;
    private double angular_velocity;
    private double x_acceleration;
    private double y_acceleration;
    private double angular_acceleration;

    // Getters and setters
    public double getTime() {
        return time;
    }

    public void setTime(double time) {
        this.time = time;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getAngle() {
        return angle;
    }

    public void setAngle(double angle) {
        this.angle = angle;
    }

    public double getXVelocity() {
        return x_velocity;
    }

    public void setXVelocity(double x_velocity) {
        this.x_velocity = x_velocity;
    }

    public double getYVelocity() {
        return y_velocity;
    }

    public void setYVelocity(double y_velocity) {
        this.y_velocity = y_velocity;
    }

    public double getAngularVelocity() {
        return angular_velocity;
    }

    public void setAngularVelocity(double angular_velocity) {
        this.angular_velocity = angular_velocity;
    }

    public double getXAcceleration() {
        return x_acceleration;
    }

    public void setXAcceleration(double x_acceleration) {
        this.x_acceleration = x_acceleration;
    }

    public double getYAcceleration() {
        return y_acceleration;
    }

    public void setYAcceleration(double y_acceleration) {
        this.y_acceleration = y_acceleration;
    }

    public double getAngularAcceleration() {
        return angular_acceleration;
    }

    public void setAngularAcceleration(double angular_acceleration) {
        this.angular_acceleration = angular_acceleration;
    }
}
