package org.firstinspires.ftc.teamcode.PathingTool;

public class PathPoint {
    private double x;
    private double y;

    public PathPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
